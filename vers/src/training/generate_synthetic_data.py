def _make(pts_dict: dict[int, list[float]]) -> np.ndarray:
    """Build a 21×3 landmark array from a sparse dict of {idx: [x,y,z]}."""
    arr = np.zeros((21, 3), dtype=np.float32)
    for idx, coords in pts_dict.items():
        arr[idx] = coords
    return arr


