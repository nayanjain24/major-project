def _normalise(landmarks: np.ndarray) -> np.ndarray:
    """Wrist-centre and scale-normalise."""
    v = landmarks.copy()
    v -= v[0]
    scale = np.max(np.abs(v))
    if scale > 1e-6:
        v /= scale
    return v.flatten()


def _generate_sequence(base_pose: np.ndarray, noise_scale: float = 0.02) -> np.ndarray:
    """Generate one 30-frame sequence with low noise for clean training."""
    frames = []
    for t in range(SEQUENCE_LENGTH):
        frame = base_pose.copy()
        # Small spatial jitter
        frame += np.random.randn(21, 3).astype(np.float32) * noise_scale
        # Gentle temporal sway
        sway = np.sin(2 * np.pi * t / SEQUENCE_LENGTH) * noise_scale * 0.5
        frame[:, 1] += sway
        frames.append(_normalise(frame))
    return np.array(frames, dtype=np.float32)


def generate_all() -> None:
    """Generate training data for all 15 emergency words."""
    # Clear old data first
    if DATA_DIR.exists():
        shutil.rmtree(DATA_DIR)
    DATA_DIR.mkdir(parents=True, exist_ok=True)

    print("=" * 60)
    print("Generating Unique Synthetic Sign Language Data")
    print("=" * 60)

    total = 0
    for word in SIGN_VOCABULARY:
        if word == "NONE":
            continue

        base_pose = UNIQUE_POSES[word]
        word_dir = DATA_DIR / word
        word_dir.mkdir(parents=True, exist_ok=True)

        for i in range(SAMPLES_PER_WORD):
            seq = _generate_sequence(base_pose, noise_scale=0.02)
            np.save(word_dir / f"seq_{i:03d}.npy", seq)

        total += SAMPLES_PER_WORD
        print(f"  ✅ {word:12s} — {SAMPLES_PER_WORD} sequences")

    print(f"\nTotal: {total} sequences across {len(UNIQUE_POSES)} unique poses")
    print(f"Saved to: {DATA_DIR}")


if __name__ == "__main__":
    generate_all()
