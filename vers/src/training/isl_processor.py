def _make(pts_dict: dict[int, list[float]]) -> np.ndarray:
    arr = np.zeros((21, 3), dtype=np.float32)
    for idx, coords in pts_dict.items():
        arr[idx] = coords
    return arr


def _normalise(landmarks: np.ndarray) -> np.ndarray:
    v = landmarks.copy()
    v -= v[0]
    scale = np.max(np.abs(v))
    if scale > 1e-6:
        v /= scale
    return v.flatten()


