def generate_isl_data(samples_per_word: int = 60) -> None:
    """Generate ISL-variant training data for all emergency words."""
    for word, base_pose in ISL_POSES.items():
        out_dir = OUTPUT_DIR / word
        out_dir.mkdir(parents=True, exist_ok=True)
        existing = len(list(out_dir.glob("isl_*.npy")))

        for i in range(samples_per_word):
            frames = []
            scale = np.random.uniform(0.8, 1.2)
            angle = np.random.uniform(-0.25, 0.25)
            cos_r, sin_r = np.cos(angle), np.sin(angle)
            rot = np.array([[cos_r, -sin_r, 0], [sin_r, cos_r, 0], [0, 0, 1]], dtype=np.float32)

            for t in range(SEQUENCE_LENGTH):
                frame = base_pose.copy() * scale
                frame = frame @ rot.T
                frame += np.random.randn(21, 3).astype(np.float32) * 0.025
                sway = np.sin(2 * np.pi * t / SEQUENCE_LENGTH) * 0.012
                frame[:, 1] += sway
                frames.append(_normalise(frame))

            seq = np.array(frames, dtype=np.float32)
            np.save(out_dir / f"isl_{existing + i:04d}.npy", seq)

        logger.info("  %s: +%d ISL sequences", word, samples_per_word)


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO, format="%(asctime)s [%(levelname)s] %(message)s")

    print("=" * 60)
    print("ISL (Indian Sign Language) Data Generator for VERS")
    print("=" * 60)
    generate_isl_data(samples_per_word=60)
    print("✅ ISL data generation complete.")
