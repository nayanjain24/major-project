"""Indian Sign Language (ISL) dataset processor for VERS.

Processes ISL datasets for regional adaptation of the VERS sign language
model. ISL differs from ASL in hand shapes and grammar, so this module
provides ISL-specific pose variants for the emergency vocabulary.

ISL datasets available:
  - INCLUDE: Indian SL recognition dataset (IIT Bombay)
  - ISL-CSLTR: Continuous SL from IIT Madras
  - Custom ISL recordings via collect_sign_data.py

Usage::
    python -m src.training.isl_processor
"""

